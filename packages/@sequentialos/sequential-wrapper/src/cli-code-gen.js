import { detectCredentials } from './auto-detect.js';
import { nowISO } from '@sequentialos/sequential-utils/timestamps';

export function generateServiceCode(libraryName) {
  return `import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { processSdkRequest } from "https://esm.sh/sdk-http-wrapper/server";
import config from "./config.json" assert { type: "json" };

const handler = async (req) => {
  const url = new URL(req.url);

  if (req.method === "GET" && url.pathname === "/health") {
    return new Response(JSON.stringify({
      status: "ok",
      service: "${libraryName}",
      timestamp: nowISO()
    }), {
      status: 200,
      headers: { "Content-Type": "application/json" }
    });
  }

  if (req.method === "POST" && url.pathname === "/call") {
    const requestData = await req.json();
    const result = await processSdkRequest(requestData, config);

    return new Response(JSON.stringify(result.body), {
      status: result.status,
      headers: { "Content-Type": "application/json" }
    });
  }

  return new Response(JSON.stringify({ error: "Not found" }), {
    status: 404,
    headers: { "Content-Type": "application/json" }
  });
};

if (import.meta.main) {
  const port = parseInt(Deno.env.get("PORT") || "${3100}");
  serve(handler, { port });
  logger.info(\`[${libraryName}] Server listening on port \${port}\`);
}
`;
}

